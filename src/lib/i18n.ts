export function pickLangText(
  lang: Lang,
  baseTr: string | null | undefined,
  en?: string | null,
  fr?: string | null
): string {
  if (lang === 'en') return (en || baseTr || '').toString()
  if (lang === 'fr') return (fr || baseTr || '').toString()
  return (baseTr || '').toString()
}

